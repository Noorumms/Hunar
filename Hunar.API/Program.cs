using Hunar.Infrastructure.Data;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// ?? DATABASE ????????????????????????????????????????????????????
// Tell EF Core to use SQL Server with our connection string
// This registers HunarDbContext as a service — it can now be
// injected anywhere via constructor injection
builder.Services.AddDbContext<HunarDbContext>(options =>
    options.UseSqlServer(builder.Configuration.GetConnectionString("DefaultConnection")));

// ?? CONTROLLERS ?????????????????????????????????????????????????
builder.Services.AddControllers();

// ?? SWAGGER (API documentation) ??????????????????????????????????
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

var app = builder.Build();

// ?? MIDDLEWARE PIPELINE ??????????????????????????????????????????
// Order matters here — top runs first on request, bottom runs first on response
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();
app.UseAuthorization();
app.MapControllers();

app.Run();